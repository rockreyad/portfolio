"use client";
import { m } from "motion/react";
import { useId } from "react";
import { usePrefersReducedMotion } from "@/hooks/use-prefers-reduced-motion";

/**
 * Hand-drawn "Hasan" signature.
 *
 * Strategy: the source SVG uses *filled* glyphs (not strokes), so we can't
 * stroke-draw it. Instead we render the signature twice:
 *  1. A muted "ink trace" at low opacity (the static, always-visible signature).
 *  2. A masked overlay where a vertical Ember light band sweeps left → right,
 *     making the signature appear lit by a moving light source.
 *
 * Reduced-motion: only the static ink trace renders.
 *
 * Accessibility: the signature is decorative when the name is also rendered
 * as text nearby; pass `label` to attach an aria-label otherwise.
 */
export function Signature({
  className,
  label,
  size = "lg",
}: {
  className?: string;
  /** If provided, the signature becomes labelled imagery; otherwise decorative. */
  label?: string;
  size?: "md" | "lg" | "xl";
}) {
  const reduced = usePrefersReducedMotion();
  const id = useId().replace(/:/g, "");
  const sweepId = `sig-sweep-${id}`;
  const maskId = `sig-mask-${id}`;
  const inkId = `sig-ink-${id}`;

  const widthClass =
    size === "xl"
      ? "w-full max-w-[min(92vw,900px)]"
      : size === "lg"
        ? "w-full max-w-[min(80vw,560px)]"
        : "w-full max-w-[min(70vw,360px)]";

  return (
    <span
      className={`relative inline-block ${widthClass} ${className ?? ""}`}
      role={label ? "img" : "presentation"}
      aria-label={label}
      aria-hidden={label ? undefined : true}
    >
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 150 75"
        preserveAspectRatio="xMidYMid meet"
        className="block h-auto w-full overflow-visible"
      >
        <defs>
          {/* Ink color of the static trace — picks up theme accent + fg. */}
          <linearGradient id={inkId} x1="0" y1="0" x2="1" y2="0">
            <stop offset="0" stopColor="var(--color-fg)" stopOpacity="0.55" />
            <stop offset="1" stopColor="var(--color-fg)" stopOpacity="0.55" />
          </linearGradient>

          {/* Light sweep — narrow vertical band that animates left to right. */}
          <linearGradient
            id={sweepId}
            x1="0"
            y1="0"
            x2="1"
            y2="0"
            gradientUnits="objectBoundingBox"
          >
            <stop offset="0" stopColor="white" stopOpacity="0" />
            <stop offset="0.45" stopColor="white" stopOpacity="0" />
            <stop offset="0.5" stopColor="var(--color-accent)" stopOpacity="1" />
            <stop offset="0.55" stopColor="white" stopOpacity="0" />
            <stop offset="1" stopColor="white" stopOpacity="0" />
          </linearGradient>

          {/* Glyph mask: the signature shape acts as the cutout. */}
          <mask id={maskId} maskUnits="userSpaceOnUse">
            <SignatureGlyphs fill="white" />
          </mask>
        </defs>

        {/* Layer 1 — muted always-on ink trace. */}
        <SignatureGlyphs fill={`url(#${inkId})`} />

        {/* Layer 2 — the moving light band, masked to the glyph shape. */}
        {!reduced && (
          <m.g mask={`url(#${maskId})`}>
            <m.rect
              x={-30}
              y={0}
              width={60}
              height={75}
              fill={`url(#${sweepId})`}
              initial={{ x: -60 }}
              animate={{ x: 150 }}
              transition={{
                duration: 3.6,
                ease: [0.65, 0, 0.35, 1],
                repeat: Infinity,
                repeatDelay: 2.4,
              }}
            />
          </m.g>
        )}

        {/* Layer 3 — a subtle full-strength "settled" overlay so signature
            looks confident even between sweeps. Opacity respects reduced-motion. */}
        {!reduced && (
          <m.g
            initial={{ opacity: 0 }}
            animate={{ opacity: 0.85 }}
            transition={{ duration: 1.2, ease: [0.16, 1, 0.3, 1] }}
          >
            <SignatureGlyphs fill="var(--color-fg)" opacity={0} />
          </m.g>
        )}
      </svg>
    </span>
  );
}

